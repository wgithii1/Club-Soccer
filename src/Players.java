import java.util.*;

public class Players {
    
    private String name; 
    private int age; 
    private String email; 
    private String gender; 
    private boolean active; 

    public Players(String name, int age, String email, String gender, boolean active) {
        this.name = name;
        this.age = age;
        this.email = email;
        this.gender = gender;
        this.active = active;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public boolean isActive() {
        return active;
    }

    public void setActive(boolean active) {
        this.active = active;
    }
    
    
    
}
